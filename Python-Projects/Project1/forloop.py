friends = ["Mia Khalifa", "Bob Marler"]


for friend in friends:
    print(friend)

print("\n")

for index in range(len(friends)):
    print(index)

print("\n")

for index in range(len(friends)):
    print(friends[index])

print("\n")

for index in range(1):
    print(friends[index])

print("\n")

for index in range(5, 10):
    print(index)

print("\nDone with loop..!!!!")